package model;

public class Gestore {
	
	private String username;
	private String pass_word;
	
	public Gestore() {
	}

	public Gestore(String user, String pass) {
		username = user;
		pass_word = pass;
	}

	public String getUsername() {
		return username;
	}

	public void setString(String user) {
		username=user;
	}

	public String getPass_word() {
		return pass_word;
	}

	public void setPass_word(String pass) {
		pass_word = pass;
	}

	
	
	
	
	
	

}
